cell3 = md(
    r"## 1. Theory: Molecular Properties from DFT" + "\n"
    "\n"
    r"### 1.1 Dipole Moment" + "\n"
    "\n"
    "The molecular dipole moment measures the asymmetry of the charge distribution:\n"
    "\n"
    r"$$\boldsymbol{\mu} = -\int \mathbf{r}\, n(\mathbf{r})\, d\mathbf{r} + \sum_I Z_I \mathbf{R}_I$$" + "\n"
    "\n"
    r"The magnitude in Debye: $\mu = |\boldsymbol{\mu}|$, where 1 D = 0.3934 a.u." + "\n"
    "\n"
    r"### 1.2 HOMO-LUMO Gap and Chemical Reactivity" + "\n"
    "\n"
    "From Koopmans' theorem extended to DFT (Janak's theorem):\n"
    "\n"
    r"$$IE \approx -\epsilon_{HOMO} \quad \text{(ionization energy)}$$" + "\n"
    r"$$EA \approx -\epsilon_{LUMO} \quad \text{(electron affinity, approximate)}$$" + "\n"
    "\n"
    "**Conceptual DFT** (Parr and Yang) defines:\n"
    r"- **Chemical potential**: $\mu_{chem} = \frac{\epsilon_{HOMO} + \epsilon_{LUMO}}{2} \approx -\frac{IE + EA}{2}$" + "\n"
    r"- **Chemical hardness**: $\eta = \frac{\epsilon_{LUMO} - \epsilon_{HOMO}}{2} \approx \frac{IE - EA}{2}$" + "\n"
    r"- **Electrophilicity index**: $\omega = \frac{\mu_{chem}^2}{2\eta}$" + "\n"
    "\n"
    "Soft molecules (small η) are more reactive; hard molecules are less reactive.\n"
    "\n"
    r"### 1.3 Fukui Functions" + "\n"
    "\n"
    "The **Fukui function** describes site-reactivity for electron addition/removal:\n"
    "\n"
    r"$$f^+(\mathbf{r}) = n_{N+1}(\mathbf{r}) - n_N(\mathbf{r}) \quad \text{(nucleophilic attack)}$$" + "\n"
    r"$$f^-(\mathbf{r}) = n_N(\mathbf{r}) - n_{N-1}(\mathbf{r}) \quad \text{(electrophilic attack)}$$" + "\n"
    r"$$f^0(\mathbf{r}) = \frac{f^+(\mathbf{r}) + f^-(\mathbf{r})}{2} \quad \text{(radical attack)}$$" + "\n"
    "\n"
    r"Using **condensed Fukui functions** from Mulliken charges $q_k$:" + "\n"
    r"$$f_k^+ = q_k(N+1) - q_k(N)$$" + "\n"
    r"$$f_k^- = q_k(N) - q_k(N-1)$$" + "\n"
    "\n"
    r"### 1.4 Electrostatic Potential (ESP)" + "\n"
    "\n"
    "The ESP measures the interaction energy between the molecular charge distribution\n"
    r"and a unit positive test charge at position $\mathbf{r}$:" + "\n"
    "\n"
    r"$$V(\mathbf{r}) = \sum_I \frac{Z_I}{|\mathbf{r}-\mathbf{R}_I|} - \int \frac{n(\mathbf{r}')}{|\mathbf{r}-\mathbf{r}'|} d\mathbf{r}'$$" + "\n"
    "\n"
    "- **Negative ESP** (red): electron-rich regions → sites for electrophilic attack\n"
    "- **Positive ESP** (blue): electron-poor regions → sites for nucleophilic attack\n"
    "- ESP mapped onto the molecular isodensity surface gives an **ESP map**"
)

