cell8 = md(
    "## 2. NMR Chemical Shielding (Brief Overview)\n"
    "\n"
    "### Nuclear Magnetic Resonance (NMR) from DFT\n"
    "\n"
    "NMR chemical shifts arise from the electronic shielding of the nuclear magnetic moment\n"
    "by the surrounding electrons. DFT can compute the **isotropic chemical shielding tensor**:\n"
    "\n"
    r"$$\sigma_{iso} = \frac{1}{3}(\sigma_{xx} + \sigma_{yy} + \sigma_{zz})$$" + "\n"
    "\n"
    r"The chemical shift relative to a reference compound (TMS for ¹H/¹³C):" + "\n"
    r"$$\delta = \sigma_{ref} - \sigma_{iso}$$" + "\n"
    "\n"
    "### ORCA NMR Input\n"
    "\n"
    "```\n"
    "! B3LYP def2-TZVP NMR\n"
    "\n"
    "%eprnmr\n"
    "  Nuclei = all C { shift }    # 13C shielding\n"
    "  Nuclei = all H { shift }    # 1H shielding\n"
    "  GIAO = true                  # Gauge-including atomic orbitals (recommended)\n"
    "end\n"
    "\n"
    "* xyz 0 1\n"
    "  [molecule coordinates]\n"
    "*\n"
    "```\n"
    "\n"
    "### Key considerations for NMR calculations:\n"
    "\n"
    "| Aspect | Recommendation |\n"
    "|--------|---------------|\n"
    "| Functional | B3LYP, PBE0, or ωB97X-D |\n"
    "| Basis set | **def2-TZVP** minimum; pcSseg-2 or pcS-2 specialized NMR basis |\n"
    "| Gauge | Use **GIAO** (gauge-including atomic orbitals) always |\n"
    "| Reference | Calculate σ(TMS) at same level; δ = σ(TMS) - σ(molecule) |\n"
    "| Solvent | Include implicit solvation (CPCM) for solution-phase spectra |\n"
    "\n"
    "### Typical accuracy (¹H shifts):\n"
    "- B3LYP/def2-TZVP: ±0.2-0.5 ppm for most organic molecules\n"
    "- ωB97X-D/def2-TZVP: ±0.15-0.3 ppm\n"
    "\n"
    "**See Chapter 12** of the course notes for a full ORCA NMR tutorial with worked examples."
)

