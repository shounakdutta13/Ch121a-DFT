        r"## 1. Theory: Hartree-Fock Approximation"
        "\n\n"
        r"### 1.1 The Variational Principle and Slater Determinants"
        "\n\n"
        r"The exact many-electron wavefunction $\Psi(x_1, x_2, \ldots, x_N)$ is approximated"
        "\n"
        r"in HF as a single **Slater determinant** of one-electron spin-orbitals $\chi_i$:"
        "\n\n"
        r"$$\Psi_{HF} = \frac{1}{\sqrt{N!}} \begin{vmatrix} \chi_1(x_1) & \chi_2(x_1) & \cdots & \chi_N(x_1) \\ \chi_1(x_2) & \chi_2(x_2) & \cdots & \chi_N(x_2) \\ \vdots & \vdots & \ddots & \vdots \\ \chi_1(x_N) & \chi_2(x_N) & \cdots & \chi_N(x_N) \end{vmatrix}$$"
        "\n\n"
        r"The Slater determinant **automatically satisfies the Pauli antisymmetry principle**."
        "\n\n"
        r"### 1.2 The Fock Operator and HF Equations"
        "\n\n"
        r"Applying the variational principle to the Slater determinant yields the"
        "\n"
        r"**Hartree-Fock equations** (Roothan equations in the LCAO-MO basis):"
        "\n\n"
        r"$$\hat{F} |\phi_i\rangle = \epsilon_i |\phi_i\rangle$$"
        "\n\n"
        r"The Fock operator is:"
        "\n"
        r"$$\hat{F} = \hat{h} + \sum_j [2\hat{J}_j - \hat{K}_j]$$"
        "\n\n"
        r"where:"
        "\n"
        r"- $\hat{h}$ = one-electron (core) Hamiltonian (kinetic + nuclear attraction)"
        "\n"
        r"- $\hat{J}_j$ = Coulomb operator: $\hat{J}_j \phi_i(1) = \left[\int |\phi_j(2)|^2 r_{12}^{-1} d2\right] \phi_i(1)$"
        "\n"
        r"- $\hat{K}_j$ = Exchange operator: $\hat{K}_j \phi_i(1) = \left[\int \phi_j^*(2) \phi_i(2) r_{12}^{-1} d2\right] \phi_j(1)$"
        "\n\n"
        r"### 1.3 SCF Procedure"
        "\n\n"
        r"Because $\hat{F}$ depends on $\{\phi_i\}$, the equations are solved iteratively:"
        "\n\n"
        r"1. **Initial guess** for density matrix $\mathbf{P}$"
        "\n"
        r"2. **Build** Fock matrix $F_{\mu\nu} = H_{\mu\nu}^{core} + \sum_{\lambda\sigma} P_{\lambda\sigma} [(\mu\nu|\sigma\lambda) - \frac{1}{2}(\mu\lambda|\sigma\nu)]$"
        "\n"
        r"3. **Solve** $\mathbf{FC} = \mathbf{SC}\boldsymbol{\epsilon}$ (generalized eigenvalue problem)"
        "\n"
        r"4. **Update** density matrix $P_{\mu\nu} = 2\sum_{i}^{occ} C_{\mu i} C_{\nu i}$"
        "\n"
        r"5. **Check convergence** — repeat from step 2 if not converged"
        "\n\n"
        r"### 1.4 Koopmans' Theorem"
        "\n\n"
        r"The negative of the HF orbital energy approximates the ionization energy:"
        "\n"
        r"$$IE_i \approx -\epsilon_i$$"
        "\n\n"
        r"This is **Koopmans' theorem** — it assumes the other orbitals do not relax upon ionization"
        "\n"
        r"(the ""frozen orbital"" approximation). It is exact within HF but neglects orbital relaxation"
        "\n"
        r"and correlation effects."
        "\n\n"
        r"### 1.5 What HF misses: Electron Correlation"
