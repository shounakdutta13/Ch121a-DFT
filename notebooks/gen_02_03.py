        "\n\n"
        r"The exact $E_{xc}[n]$ is unknown. All DFT methods differ in how they approximate it."
        "\n\n"
        r"$$E_{xc}[n] = E_x[n] + E_c[n]$$"
        "\n\n"
        r"**Exchange energy** $E_x$ accounts for the Fermi hole (Pauli antisymmetry),"
        "\n"
        r"**correlation energy** $E_c$ captures dynamic and static electron correlation."
