        "\n\n"
        r"**Theorem 1 (Existence):** The external potential $v_{ext}(\mathbf{r})$ — and hence the"
        "\n"
        r"total energy — is a unique functional of the ground-state electron density $n_0(\mathbf{r})$:"
        "\n\n"
        r"$$E = E[n_0]$$"
        "\n\n"
        r"**Theorem 2 (Variational principle):** The universal functional $F[n]$ delivers"
        "\n"
        r"the ground-state energy if and only if the true ground-state density is used:"
        "\n\n"
        r"$$E[n] \geq E_0 \quad \text{for all } n \neq n_0$$"
        "\n\n"
        r"These theorems establish that $n(\mathbf{r})$ — a function of only 3 spatial variables"
        "\n"
        r"— contains all information needed to determine the ground-state energy."
        "\n\n"
        r"### 1.2 Kohn-Sham DFT (1965)"
        "\n\n"
        r"The practical implementation by Kohn and Sham introduces a fictitious non-interacting"
        "\n"
        r"reference system with the **same density** as the real interacting system."
        "\n\n"
        r"The Kohn-Sham equations are:"
        "\n\n"
        r"$$\left[-\frac{1}{2}\nabla^2 + v_{eff}(\mathbf{r})\right]\phi_i(\mathbf{r}) = \epsilon_i \phi_i(\mathbf{r})$$"
        "\n\n"
        r"where the effective potential is:"
        "\n\n"
        r"$$v_{eff}(\mathbf{r}) = v_{ext}(\mathbf{r}) + \underbrace{\int \frac{n(\mathbf{r}')}{|\mathbf{r}-\mathbf{r}'|} d\mathbf{r}'}_{v_{Hartree}} + \underbrace{v_{xc}[n](\mathbf{r})}_{exchange-correlation}$$"
        "\n\n"
        r"The total DFT energy is:"
        "\n\n"
        r"$$E_{KS}[n] = T_s[n] + E_{ne}[n] + J[n] + E_{xc}[n]$$"
        "\n\n"
        r"where $T_s$ = kinetic energy of non-interacting electrons, $E_{ne}$ = nuclear-electron"
        "\n"
