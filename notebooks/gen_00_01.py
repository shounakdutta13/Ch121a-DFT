nb01_cell3 = md(
    "## 1. Theory: Basis Functions in Quantum Chemistry\n"
    "\n"
    "### 1.1 Slater-Type Orbitals (STOs)\n"
    "\n"
    "The exact hydrogen-like atomic orbitals are Slater-type orbitals (STOs):\n"
    "\n"
    r"$$\chi_{nlm}(r,\theta,\phi) = N r^{n-1} e^{-\zeta r} Y_l^m(\theta,\phi)$$"
    "\n"
    "\n"
    "where:\n"
    "- $N$ = normalization constant\n"
    "- $n$ = principal quantum number\n"
    r"- $\zeta$ (zeta) = orbital exponent controlling size"
    "\n"
    r"- $Y_l^m$ = spherical harmonic"
    "\n"
    "\n"
    "**Advantages:** Correct cusp at nucleus, correct exponential decay\n"
    "**Disadvantage:** Multi-center two-electron integrals are very expensive (no analytic formula)\n"
    "\n"
    "### 1.2 Gaussian-Type Orbitals (GTOs)\n"
    "\n"
    "In practice, we use Gaussian-type orbitals (GTOs):\n"
    "\n"
    r"$$g_{lmn}(\mathbf{r}; \alpha) = N x^l y^m z^n e^{-\alpha r^2}$$"
    "\n"
    "\n"
    r"where $\alpha$ is the Gaussian exponent and $l+m+n$ determines the angular momentum."
    "\n"
    "\n"
    "**Advantage:** Products of Gaussians are Gaussians → all integrals are analytic!\n"
    "**Disadvantage:** Incorrect behavior at nucleus (no cusp) and at long range (decays too fast)\n"
    "\n"
    "### 1.3 Contracted Basis Sets\n"
    "\n"
    "To capture STO-like behavior, we use **contracted** basis functions:\n"
    r"$$\chi_\mu = \sum_k d_{k\mu} g_k$$"
    "\n"
    "\n"
    r"A **contraction** combines multiple primitive Gaussians ($g_k$) with fixed coefficients ($d_{k\mu}$) to form a single basis function. For example, STO-3G uses 3 Gaussians to approximate each STO."
    "\n"
    "\n"
    "### 1.4 Basis Set Families\n"
    "\n"
    "| Family | Strategy | Example |\n"
    "|--------|----------|---------|\n"
    "| **Pople** | Add shells systematically | 6-31G, 6-31G\\*, 6-311G\\*\\* |\n"
    "| **Dunning** (cc-pVxZ) | Converges to CBS limit | cc-pVDZ, cc-pVTZ, cc-pVQZ |\n"
    "| **Ahlrichs** (def2) | Balanced accuracy/cost | def2-SVP, def2-TZVP, def2-QZVP |\n"
    "\n"
    "**Notation:**\n"
    "- **DZ** (double-ζ), **TZ** (triple-ζ), **QZ** (quadruple-ζ): number of basis functions per orbital\n"
    "- **\\*** or **P**: adds polarization functions (e.g., d functions on C, N, O)\n"
    "- **aug-**: adds diffuse functions (needed for anions, excited states, weak interactions)"
)

