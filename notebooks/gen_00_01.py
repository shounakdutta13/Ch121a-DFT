nb01_cell7 = code(
    "# ------------------------------------------------------------------\n"
    "# Radial Probability Distributions of Hydrogen-like Orbitals\n"
    "# ------------------------------------------------------------------\n"
    "# P(r) = r^2 |R_nl(r)|^2  (probability per unit radius)\n"
    "\n"
    "import matplotlib\n"
    "matplotlib.rcParams['figure.dpi'] = 120\n"
    "import matplotlib.pyplot as plt\n"
    "import numpy as np\n"
    "from scipy.special import factorial, genlaguerre\n"
    "\n"
    "def hydrogen_radial(n, l, r, Z=1):\n"
    "    \"\"\"Normalized radial wavefunction R_nl(r) for hydrogen-like atom.\"\"\"\n"
    "    rho = 2 * Z * r / n\n"
    "    # Normalization constant\n"
    "    norm = np.sqrt((2*Z/n)**3 * factorial(n-l-1) /\n"
    "                   (2*n * factorial(n+l)**3))\n"
    "    # Associated Laguerre polynomial\n"
    "    L = genlaguerre(n-l-1, 2*l+1)(rho)\n"
    "    return norm * np.exp(-rho/2) * rho**l * L\n"
    "\n"
    "r = np.linspace(0.001, 30, 2000)  # Bohr radii\n"
    "\n"
    "orbitals = [\n"
    "    (1, 0, '1s', 'steelblue',   '-'),\n"
    "    (2, 0, '2s', 'coral',        '-'),\n"
    "    (2, 1, '2p', 'seagreen',    '-'),\n"
    "    (3, 0, '3s', 'mediumpurple', '--'),\n"
    "    (3, 1, '3p', '#E67E22',      '--'),\n"
    "    (3, 2, '3d', '#E74C3C',      '--'),\n"
    "]\n"
    "\n"
    "fig, axes = plt.subplots(1, 2, figsize=(13, 5))\n"
    "\n"
    "# Left plot: all orbitals together\n"
    "ax = axes[0]\n"
    "for n, l, label, color, ls in orbitals:\n"
    "    R = hydrogen_radial(n, l, r)\n"
    "    P = r**2 * R**2\n"
    "    ax.plot(r, P, label=f'${label}$', color=color, linestyle=ls, linewidth=2)\n"
    "ax.set_xlim(0, 25)\n"
    "ax.set_ylim(bottom=0)\n"
    "ax.set_xlabel('Radius $r$ (Bohr)', fontsize=12)\n"
    "ax.set_ylabel('Radial Probability $P(r) = r^2|R_{nl}|^2$', fontsize=11)\n"
    "ax.set_title('Hydrogen-like Radial Probability Distributions', fontsize=12)\n"
    "ax.legend(fontsize=11, ncol=2)\n"
    "ax.grid(True, alpha=0.3)\n"
    "\n"
    "# Right plot: n=1,2,3 s orbitals only (compare size)\n"
    "ax2 = axes[1]\n"
    "for n, l, label, color, ls in orbitals[:3]:\n"
    "    R = hydrogen_radial(n, l, r)\n"
    "    P = r**2 * R**2\n"
    "    # Mark the most probable radius\n"
    "    r_mp = r[np.argmax(P)]\n"
    "    ax2.plot(r, P, label=f'${label}$ ($r_{{mp}}$={r_mp:.1f} $a_0$)',\n"
    "             color=color, linewidth=2.5)\n"
    "    ax2.axvline(x=r_mp, color=color, linestyle=':', alpha=0.6)\n"
    "ax2.set_xlim(0, 15)\n"
    "ax2.set_ylim(bottom=0)\n"
    "ax2.set_xlabel('Radius $r$ (Bohr)', fontsize=12)\n"
    "ax2.set_ylabel('Radial Probability $P(r)$', fontsize=11)\n"
    "ax2.set_title('1s, 2s, 2p: Most Probable Radii', fontsize=12)\n"
    "ax2.legend(fontsize=10)\n"
    "ax2.grid(True, alpha=0.3)\n"
    "\n"
    "plt.tight_layout()\n"
    "plt.show()\n"
    "print(\"\\nNote: 1 Bohr = 0.529 Å\")\n"
    "print(\"Most probable radius for 1s: 1 Bohr (= Bohr radius a₀)\")"
)

