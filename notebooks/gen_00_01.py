    "%%time\n"
    "# ------------------------------------------------------------------\n"
    "# Basis set convergence study for water (H2O)\n"
    "# ------------------------------------------------------------------\n"
    "# We compare HF energies across a range of basis sets to see\n"
    "# how the energy converges toward the complete basis set (CBS) limit.\n"
    "\n"
    "mol_template = \"\"\"\n"
    "O   0.000000   0.000000   0.117176\n"
    "H   0.000000   0.757001  -0.468704\n"
    "H   0.000000  -0.757001  -0.468704\n"
    "\"\"\"\n"
    "\n"
    "basis_sets = ['STO-3G', '3-21G', '6-31G*', 'cc-pVDZ', 'cc-pVTZ', 'def2-SVP', 'def2-TZVP']\n"
    "\n"
    "results = []\n"
    "for basis in basis_sets:\n"
    "    mol = gto.Mole()\n"
    "    mol.atom = mol_template\n"
    "    mol.basis = basis\n"
    "    mol.verbose = 0\n"
    "    mol.build()\n"
    "    mf = scf.RHF(mol)\n"
    "    mf.verbose = 0\n"
    "    e = mf.kernel()\n"
    "    n_bas = mol.nao_nr()\n"
    "    results.append({'Basis Set': basis, 'N_basis': n_bas,\n"
    "                    'E_HF (Ha)': round(e, 8), 'E_HF (eV)': round(e*27.2114, 5)})\n"
    "    print(f\"  {basis:12s}  N={n_bas:3d}  E = {e:.8f} Ha\")\n"
